# if / elif / else
condicao = False
condicao2 = False
condicao3 = True
condicao4 = False

if condicao:
    print('Este código do if')
elif condicao2:
    print('Código para condicao2')
elif condicao3:
    print('Código para condicao3')
elif condicao4:
    print('Código para condicao4')
else:
    print('Nenhuma condição foi satisfeita')

if 10 == 10:
    print('If do 10')